import { toast } from 'react-toastify';
import { GET_EMAIL_TYPES,GET_ERRORS,GET_EMAIL_TYPE, CREATE_EMAIL_TYPE, EDIT_EMAIL_TYPE,BEFORE_EMAIL_TYPE, DELETE_EMAIL_TYPE} from '../../redux/types';
import { emptyError } from '../../redux/shared/error/error.action';
import { ENV } from '../../config/config';

export const beforeEmailType = () => {
    return {
        type: BEFORE_EMAIL_TYPE
    }
}

export const getEmailTypes = (qs = '', body ={}) => dispatch => {
    dispatch(emptyError());
    let url = `${ENV.url}emailType/list`;
    if (qs)
        url += `?${qs}`

    fetch(url, {
        method: 'POST',
        headers: {
            'content-type': 'application/json',
            'Authorization': ENV.Authorization,
            'x-auth-token': ENV.x_auth_token
        },
        body: JSON.stringify(body)
    }).then(res => res.json()).then(data => {
        if (data.success) {
            if (!qs)
                toast.success(data.message)
            dispatch({
                type: GET_EMAIL_TYPES,
                payload: data.data
            })
        } else {
            if (!qs)
                toast.error(data.message)
            dispatch({
                type: GET_ERRORS,
                payload: data
            })
        }
    }).catch(error => {
        if (error.response && error.response.data) {
            const { data } = error.response
            if (data.message)
                toast.error(data.message)
        }
        dispatch({
            type: GET_ERRORS,
            payload: error
        })
    })
};

