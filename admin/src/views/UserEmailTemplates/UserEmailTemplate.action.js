import { toast } from 'react-toastify';
import { GET_USER_EMAIL_TYPES,GET_ERRORS, BEFORE_CATEGORY, GET_CATEGORIES,GET_USER_EMAIL_TEMPLATE, UPSERT_CATEGORY, DELETE_CATEGORY, DELETE_FAQ, CREATE_USER_EMAIL_TEMPLATE, GET_USER_EMAIL_TEMPLATES, EDIT_USER_EMAIL_TEMPLATE,BEFORE_USER_EMAIL_TEMPLATE, DELETE_USER_EMAIL_TEMPLATE} from '../../redux/types';
import { emptyError } from '../../redux/shared/error/error.action';
import { ENV } from '../../config/config';

export const beforeUserEmailTemplate = () => {
    return {
        type: BEFORE_USER_EMAIL_TEMPLATE
    }
}

export const getEmailTypes = (qs = '', body ={}) => dispatch => {
    dispatch(emptyError());
    let url = `${ENV.url}template/list/email/type`;
    if (qs)
        url += `?${qs}`

    fetch(url, {
        method: 'POST',
        headers: {
            'content-type': 'application/json',
            'Authorization': ENV.Authorization,
            'x-auth-token': ENV.x_auth_token
        },
        body: JSON.stringify(body)
    }).then(res => res.json()).then(data => {
        if (data.success) {
            if (!qs)
                toast.success(data.message)
            dispatch({
                type: GET_USER_EMAIL_TYPES,
                payload: data.data
            })
        } else {
            if (!qs)
                toast.error(data.message)
            dispatch({
                type: GET_ERRORS,
                payload: data
            })
        }
    }).catch(error => {
        if (error.response && error.response.data) {
            const { data } = error.response
            if (data.message)
                toast.error(data.message)
        }
        dispatch({
            type: GET_ERRORS,
            payload: error
        })
    })
};


