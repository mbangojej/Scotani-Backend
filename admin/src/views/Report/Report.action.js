import { toast } from 'react-toastify';
import { GET_ERRORS, BEFORE_REPORT, GET_SALES_REPORT, GET_INVOICE_REPORT} from '../../redux/types';
import { emptyError } from '../../redux/shared/error/error.action';
import { ENV } from '../../config/config';

export const beforeReport = () => {
    return {
        type: BEFORE_REPORT
    }
}

export const getSalesReport = (qs = '', body ={}) => dispatch => {
    dispatch(emptyError());
    let url = `${ENV.url}report/salesReport`;
    if (qs)
        url += `?${qs}`

    fetch(url, {
        method: 'POST',
        headers: {
            'content-type': 'application/json',
            'Authorization': ENV.Authorization,
            'x-auth-token': ENV.x_auth_token
        },
        body: JSON.stringify(body)
    }).then(res => res.json()).then(data => {
        if (data.success) {
            toast.success(data.message)
            dispatch({
                type: GET_SALES_REPORT,
                payload: data
            })
        } else {
            if (!qs)
                toast.error(data.message)
            dispatch({
                type: GET_ERRORS,
                payload: data
            })
        }
    }).catch(error => {
        if (error.response && error.response.data) {
            const { data } = error.response
            if (data.message)
                toast.error(data.message)
        }
        dispatch({
            type: GET_ERRORS,
            payload: error
        })
    })
}

