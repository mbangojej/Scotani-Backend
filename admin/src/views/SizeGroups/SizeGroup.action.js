import { toast } from 'react-toastify';
import { GET_ERRORS, GET_SIZEGROUPS, BEFORE_SIZEGROUP, DELETE_SIZEGROUP, CREATE_SIZEGROUP, GET_SIZEGROUP, EDIT_SIZEGROUP} from '../../redux/types';
import { emptyError } from '../../redux/shared/error/error.action';
import { ENV } from '../../config/config';

export const beforeSizeGroup = () => {
    return {
        type: BEFORE_SIZEGROUP
    }
}

export const getSizeGroups = (qs = '', body ={}) => dispatch => {
    dispatch(emptyError());
    let url = `${ENV.url}sizeGroup/list`;
    if (qs)
        url += `?${qs}`

    fetch(url, {
        method: 'POST',
        headers: {
            'content-type': 'application/json',
            'Authorization': ENV.Authorization,
            'x-auth-token': ENV.x_auth_token
        },
        body: JSON.stringify(body)
    }).then(res => res.json()).then(data => {
        if (data.success) {
            if (!qs)
                toast.success(data.message)
            dispatch({
                type: GET_SIZEGROUPS,
                payload: data.data
            })
        } else {
            if (!qs)
                toast.error(data.message)
            dispatch({
                type: GET_ERRORS,
                payload: data
            })
        }
    }).catch(error => {
        if (error.response && error.response.data) {
            const { data } = error.response
            if (data.message)
                toast.error(data.message)
        }
        dispatch({
            type: GET_ERRORS,
            payload: error
        })
    })
};

